      timestamp: new Date().toISOString()
    }

    currentRecord.value.images.push(imageInfo)

    // Ëá™Âä®‰øùÂ≠ò
    saveRecord()
  }

  reader.onerror = () => {
    alert('ÂõæÁâáËØªÂèñÂ§±Ë¥•ÔºåËØ∑ÈáçËØï')
  }

  reader.readAsDataURL(file)
